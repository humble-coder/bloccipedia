class ApplicationPolicy
  attr_reader :user, :record

  def initialize(user, record)
    @user = user
    @record = record
  end

  def index?
    true
  end

  def show?
    scope.where(:id => record.id).exists?
  end

  def scope
    Pundit.policy_scope!(user, Wiki) # Wiki used to be wiki.class
  end
end

